package com.zebone.dip.md.vo;

/**
 * 键值对象
 */

public class NameCode {
	/**名称*/
	private String name;
	/**编码*/
	private String code;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
}
